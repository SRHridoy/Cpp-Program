#include<bits/stdc++.h>
#define ll long long int
#define nl "\n"
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);


    ll a, b;
    cin >> a >> b;
    cout << a << " " << "+" << " " << b << " " << "=" << " " << a+b << nl;
    cout << a << " " << "*" << " " << b << " " << "=" << " " << a*b << nl;
    cout << a << " " << "-" << " " << b << " " << "=" << " " << a-b << nl;


    return 0;
}