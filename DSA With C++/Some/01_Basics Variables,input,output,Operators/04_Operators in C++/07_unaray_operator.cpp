#include<bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

/* ----------UNARY PLUS---------- */
    int a = +4;
/* ----------UNARY MINUS---------- */
    int b = -5;
/* ----------POST-INCREMENT OPERATOR---------- */
    a++;
    b--;
/* ----------PRE-INCREMENT OPERATOR---------- */
    ++a;
    --b;
/* ----------LOGICAL COMPLEMENT OPERATOR---------- */
    b = !true;


    return 0;
}